#include "libft.h"

int		ft_isblank(char c)
{
	if (c == ' ' || c == '\t' || c == '\v' || c == '\r' || c == '\n'
		|| c == '\f')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
