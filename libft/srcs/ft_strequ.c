#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		res;
	size_t	i;

	res = 0;
	if (s1 && s2)
	{
		i = 0;
		while (s1[i] != '\0')
		{
			if (s1[i] != s2[i])
			{
				return (res);
			}
			i++;
		}
		res = (s2[i] != '\0') ? 0 : 1;
	}
	return (res);
}
